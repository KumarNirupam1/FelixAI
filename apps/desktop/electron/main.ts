let lastScreenshot: string | null = null;
let lastScreenContext = "";
let isPopupVisible = false;

const rendererDevUrl = process.env["ELECTRON_RENDERER_URL"];

function createPopup(): BrowserWindow {
  const win = new BrowserWindow({
    width: COMPACT_WIDTH,
    height: COMPACT_HEIGHT,
    show: false,
    frame: false,
    transparent: true,
    resizable: true,
    skipTaskbar: true,
    alwaysOnTop: true,
    hasShadow: false,
    backgroundColor: "#00000000",
    webPreferences: {
      preload: join(__dirname, "../preload/index.js"),
      contextIsolation: true,
      nodeIntegration: false,
      sandbox: false,
    },
  });

  win.setAlwaysOnTop(true, "screen-saver");

  if (rendererDevUrl) {
    void win.loadURL(rendererDevUrl);
  } else {
    void win.loadFile(join(__dirname, "../renderer/index.html"));
  }

  // Dismiss only via ESC (global shortcut, header button, or key in popup).
  win.webContents.on("before-input-event", (_e, input) => {
    if (input.type === "keyDown" && input.key === "Escape") hidePopup();
  });

  return win;
}

function centerWindow(win: BrowserWindow, width: number, height: number): void {
  win.setSize(width, height);
  win.center();
}

function expandWindow(): void {
  if (!popup || popup.isDestroyed()) return;
  const { workArea } = screen.getPrimaryDisplay();
  const width = Math.min(1000, workArea.width - 100);
  const height = Math.min(700, workArea.height - 100);
  centerWindow(popup, width, height);
}

function hidePopup(): void {
  if (!popup || popup.isDestroyed()) return;
  centerWindow(popup, COMPACT_WIDTH, COMPACT_HEIGHT);
  popup.hide();
  isPopupVisible = false;
  popup.webContents.send("vision:status", "idle");
}

function sendVisionStatus(status: "idle" | "analyzing" | "ready" | "failed"): void {
  popup?.webContents.send("vision:status", status);
}

/** OpenRouter vision on summon — screenshot stays in main, never sent to UI. */
async function analyzeScreenInBackground(): Promise<void> {
  const shot = lastScreenshot;
  const openrouterKey = process.env.OPENROUTER_API_KEY;

  if (!shot || !openrouterKey) {
    lastScreenContext = "";
    sendVisionStatus(shot ? "failed" : "idle");
    return;
  }

  sendVisionStatus("analyzing");
  try {
    lastScreenContext = await analyzeScreen({
      apiKey: openrouterKey,
      screenshotDataUrl: shot,
    });
    sendVisionStatus("ready");
  } catch (err) {
    console.error("OpenRouter vision on summon failed", err);
    lastScreenContext = "";
    sendVisionStatus("failed");
  }
}

function notifyPopupShown(): void {
  if (!popup || popup.isDestroyed()) return;
  const notify = () => {
    popup?.webContents.send("popup:shown");
    void analyzeScreenInBackground();
  };
  if (popup.webContents.isLoading()) {
    popup.webContents.once("did-finish-load", notify);
  } else {
    notify();
  }
}

/** Capture screenshot first, then show centered popup (FlickAI-style). */
async function summon(): Promise<void> {
  lastScreenshot = await captureScreenshot();
  lastScreenContext = "";

  if (!popup || popup.isDestroyed()) popup = createPopup();
  const win = popup;

  centerWindow(win, COMPACT_WIDTH, COMPACT_HEIGHT);
  win.show();
  win.focus();
  isPopupVisible = true;

  notifyPopupShown();
}

function registerHotkeys(): void {
  const registered = globalShortcut.register(HOTKEY, () => void summon());
  if (!registered) console.error("Failed to register global shortcut", HOTKEY);

  globalShortcut.register("Escape", () => {
    if (isPopupVisible) hidePopup();
  });
}

function createTray(): void {
  let icon = nativeImage.createFromDataURL(TRAY_ICON);
  if (icon.isEmpty()) icon = nativeImage.createEmpty();
  tray = new Tray(icon);
  tray.setToolTip("FelixAI — press Ctrl+Shift+Space");
  tray.setContextMenu(
    Menu.buildFromTemplate([
      { label: "Ask (Ctrl+Shift+Space)", click: () => void summon() },
      { type: "separator" },
      { label: "Quit FelixAI", click: () => app.quit() },
    ]),
  );
  tray.on("click", () => void summon());
}

/** Bridge session cache → permanent graph (background, never blocks UI). */
function runImprove(dataset: DatasetName = "main"): void {
  void cognee
    .improve({ dataset, sessionIds: [SESSION_ID] })
    .catch((err) => console.error("improve failed", err));
}

function maybeImproveAfterInteraction(dataset: DatasetName): void {
  datasetsTouched.add(dataset);
  interactionCount += 1;
  if (interactionCount % IMPROVE_EVERY_N === 0) runImprove(dataset);
}

function registerIpc(): void {
  ipcMain.handle("ask", async (_e, payload: AskPayload): Promise<AskResult> => {
    const dataset: DatasetName = payload.dataset;
    const shot = lastScreenshot;

    if (!shot) {
      return {
        answer: "No screenshot was captured for this query.",
        usedMemory: false,
      };
    }

    let screenContext = lastScreenContext;
    const openrouterKey = process.env.OPENROUTER_API_KEY;
    if (!screenContext && openrouterKey) {
      try {
        screenContext = await analyzeScreen({
          apiKey: openrouterKey,
          screenshotDataUrl: shot,
        });
        lastScreenContext = screenContext;
      } catch (err) {
        console.error("OpenRouter vision failed", err);
      }
    }

    const recallQuery = cognee.buildRecallQuery(payload.question, screenContext);
    const cogneeUp = await cognee.health();

    let answer = "";
    let usedMemory = false;

    if (cogneeUp) {
      try {
        const recalled = await cognee.recall(recallQuery, {
          dataset,
          sessionId: SESSION_ID,
          scope: "auto",
          searchType: "GRAPH_COMPLETION",
          systemPrompt: FELIX_RECALL_PROMPT,
        });
        answer = recalled.text.trim();
        usedMemory = recalled.usedMemory;
      } catch (err) {
        console.error("recall failed", err);
      }
    }

    if (!answer) {
      answer = cognee.buildFallbackAnswer(screenContext, cogneeUp);
    }

    const rememberContext =
      screenContext.slice(0, 2000) || "Screen Q&A via FelixAI";

    void cognee
      .rememberQA(
        {
          question: payload.question,
          answer,
          context: rememberContext,
        },
        { dataset, sessionId: SESSION_ID },
      )
      .then((res) => {
        popup?.webContents.send("remember:done", { qaId: res.id });
        maybeImproveAfterInteraction(dataset);
      })
      .catch((err) => console.error("remember failed", err));

    return { answer, usedMemory };
  });

  ipcMain.handle(
    "feedback",
    async (_e, qaId: string, score: number, dataset: DatasetName) => {
      try {
        if (qaId) {
          await cognee.feedback(qaId, score, { dataset, sessionId: SESSION_ID });
        }
        await cognee.improve({ dataset, sessionIds: [SESSION_ID] });
        return { ok: true };
      } catch (err) {
        return { ok: false, error: (err as Error).message };
      }
    },
  );

  ipcMain.handle("listMemory", async () => cognee.listDatasets());

  ipcMain.handle("forgetPrivate", async () => {
    try {
      await cognee.forget("private");
      return { ok: true };
    } catch (err) {
      return { ok: false, error: (err as Error).message };
    }
  });

  ipcMain.handle("getStatus", async () => ({
    cogneeUp: await cognee.health(),
    sessionId: SESSION_ID,
  }));

  ipcMain.handle("hidePopup", () => hidePopup());

  ipcMain.handle("expandWindow", () => {
    expandWindow();
  });

  ipcMain.handle("recaptureScreen", async () => {
    if (popup && !popup.isDestroyed() && popup.isVisible()) {
      popup.hide();
      await new Promise((r) => setTimeout(r, 350));
    }
    lastScreenshot = await captureScreenshot();
    lastScreenContext = "";
    if (popup && !popup.isDestroyed()) {
      popup.show();
      popup.focus();
      isPopupVisible = true;
    }
    await analyzeScreenInBackground();
  });
}

if (gotLock) {
  // If the user launches a second instance, focus/summon the existing one.
  app.on("second-instance", () => void summon());

  app.whenReady().then(() => {
    if (process.platform === "win32") {
      app.setAppUserModelId("com.felixai.companion");
    }

    void ensureCogneeRunning(process.env.COGNEE_REPO_PATH);

    registerIpc();
    popup = createPopup();
    centerWindow(popup, COMPACT_WIDTH, COMPACT_HEIGHT);
    createTray();
    registerHotkeys();
  });
}

app.on("will-quit", () => {
  globalShortcut.unregisterAll();
  // Final improve on quit — sync session → permanent graph for demo + real use.
  for (const dataset of datasetsTouched) runImprove(dataset);
});

// FelixAI stays in the tray — don't quit when the popup is hidden.
app.on("window-all-closed", () => {
  /* intentionally no-op */
});
